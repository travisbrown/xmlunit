package org.custommonkey.xmlunit;

/**
 * Contains the pure evaluation logic of DifferenceListener
 */
public interface DifferenceEvaluator {
    /**
     * Receive notification of a difference and decides whether 2
     * nodes are different.
     * @param difference a Difference instance as defined in {@link
     * DifferenceConstants DifferenceConstants} describing the cause
     * of the difference and containing the detail of the nodes that
     * differ
     * @return int one of the RETURN_...  constants of
     * DifferenceListener describing how this difference was
     * interpreted
     */
    int evaluate(Difference difference);
}
